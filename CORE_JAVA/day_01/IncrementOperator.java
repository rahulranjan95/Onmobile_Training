public class IncrementOperator {

	public static void main(String[] args) {
	
		byte a = 126;
		
		System.out.println(a);
		
		a++;
		System.out.println(a);
		
		a++;
		System.out.println(a);
		
		a++;
		System.out.println(a);
		
		byte b = (byte)128;
		System.out.println(b);
		
		System.out.println(Integer.MAX_VALUE);
		
		int i =Integer.MAX_VALUE;
		i++;
		
		System.out.println(i);
		
	}

}
